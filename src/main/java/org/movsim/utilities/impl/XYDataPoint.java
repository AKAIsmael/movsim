package org.movsim.utilities.impl;

public class XYDataPoint {

    
    private double x;
    private double y;
    
    public XYDataPoint(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    
    
}
